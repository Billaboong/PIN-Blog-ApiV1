    public function findOneBydescription($description): array
        {
           
                $conn = $this->getEntityManager()->getConnection();

                $sql = '
                    SELECT * FROM blog b
                    WHERE b.description LIKE :description
                    ORDER BY b.id ASC
                    ';
         
                $resultSet = $conn->executeQuery($sql, ['description' => '%'.addcslashes($description, '%_').'%']);
                return $resultSet->fetchAllAssociative();
        }
